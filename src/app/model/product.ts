export interface Product{ 
    name: string; 
    description: string;
    category: string; 
    price: number; 
    quantity: number; 
    imageUrl: string; 
    id: string;
} 

export function compareProductsByName(a: Product, b: Product){
    return a.name.localeCompare(b.name);
}

export function compareProductsByPrice(a: Product, b: Product){
    return a.price - b.price;
}




